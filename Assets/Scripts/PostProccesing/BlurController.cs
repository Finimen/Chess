using UnityEngine;
using UnityStandardAssets.ImageEffects;

namespace FinimenSniperC
{
    [DisallowMultipleComponent]
    internal class BlurController : MonoBehaviour
    {
        [SerializeField] private BlurOptimized blur;

        private bool blurState;

        public void ChangeBlurState()
        {
            blurState = !blurState;
        }

        private void Update()
        {
            if (blurState)
            {
                blur.enabled = true;

                blur.blurSize = Mathf.Lerp(blur.blurSize, 10, .05f);

                Time.timeScale = 0;
            }
            else
            {
                blur.blurSize = Mathf.Lerp(blur.blurSize, 0, .05f);

                Time.timeScale = 1;

                if (blur.blurSize <= 0.01)
                {
                    blur.blurSize = 0;

                    blur.enabled = false;
                }
            }
        }
    }
}