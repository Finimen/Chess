using UnityEngine;

namespace FinimenSniperC
{
    [DisallowMultipleComponent]
    internal class FigureDestoyer : MonoBehaviour
    {
        public static FigureDestoyer Instance { get; private set; }

        public void DestroyFigure(Figure figure, float time = 1)
        {
            Board.AllFigures.Remove(figure);

            LeanTween.scale(figure.gameObject, Vector3.zero, .5f).setEaseInOutBack().destroyOnComplete = true;
        }

        private void Awake()
        {
            Instance = this;
        }
    }
}