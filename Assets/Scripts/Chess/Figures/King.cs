using UnityEngine;
using System;

namespace FinimenSniperC
{
    internal class King : Figure
    {
        [SerializeField] private Location[] avaibleMoves;

        [NonSerialized] protected readonly Location[] steps = new Location[] { 
            new Location(new Vector2(0, 1)),
            new Location(new Vector2(0, -1)),
            new Location(new Vector2(1, 0)),
            new Location(new Vector2(-1, 0)),
            new Location(new Vector2(1, -1)),
            new Location(new Vector2(1, 1)),
            new Location(new Vector2(-1, 1)),
            new Location(new Vector2(-1, -1))};

        public override MoveType MoveType
        {
            get
            {
                return MoveType.King;
            }
        }

        public override Location[] Steps
        {
            get
            {
                return steps;
            }
        }

        public override void Move(Location target)
        {
            Location = target;
        }

        protected override void OnDrawGizmosSelected()
        {
            base.OnDrawGizmosSelected();
        }

        private void Update()
        {
            avaibleMoves = FindAvaibleCells();
        }
    }
}