using UnityEngine;

namespace FinimenSniperC
{
    [DisallowMultipleComponent]
    internal class ParticleCreator : MonoBehaviour
    {
        [SerializeField] private ParticleSystem destroyParticlePrefab;

        public static ParticleCreator Instance { get; private set; }

        private void Awake()
        {
            Instance = this;
        }

        private void Start()
        {
            EventHolder.OnFigureDestroyed.AddListener(CreateParticle);
        }

        public void CreateParticle(Figure figureDestroed)
        {
            Instantiate(destroyParticlePrefab, figureDestroed.transform.position, Quaternion.identity);
        }

        public void CreateParticle(Transform transform)
        {
            Instantiate(destroyParticlePrefab, transform.position, transform.rotation);
        }
    }
}