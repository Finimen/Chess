using UnityEngine;

namespace FinimenSniperC
{
    [DisallowMultipleComponent]
    internal class Teams : MonoBehaviour
    {
        public static ColorID CurrentTeamStep { get; private set; }

        private void Update()
        {
            if (Input.GetKey(KeyCode.R))
            {
                CurrentTeamStep = ColorID.Black;
            }
            else if (Input.GetKey(KeyCode.F))
            {
                CurrentTeamStep = ColorID.White;
            }
        }

        public static void UpdateStep()
        {
            CurrentTeamStep = CurrentTeamStep == ColorID.Black ? ColorID.White : ColorID.Black;

            CheckmateController.CheckMatOrGameOver();
        }

        public static bool IsMyTeamMoving(ColorID colorID)
        {
            if(colorID == CurrentTeamStep)
            {
                return true;
            }
            else
            {
                 return false;
            }
        }
    }
}